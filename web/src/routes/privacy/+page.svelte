    <section>
      <h2>How tsbin Mail Works</h2>
      <p>
        tsbin Mail is a web application that creates temporary email addresses.
        When you use tsbin Mail, all emails are stored temporarily and
        automatically deleted after 24 hours.
      </p>
      <p>No personal information is required to create a mailbox.</p>
      <p>No account registration is required.</p>
      <p>No identifiers are generated or tracked beyond the mailbox session.</p>
    </section>

    <section>
      <h2>Data Storage</h2>
      <p>
        When you create a temporary email address, the following data is stored:
      </p>
      <ul>
        <li>The email address you created</li>
        <li>Emails received at that address</li>
        <li>Expiry timestamp (24 hours from creation)</li>
      </ul>
      <p>
        All data is automatically and permanently deleted after 24 hours. There
        are no backups. There is no recovery.
      </p>
    </section>

    <section>
      <h2>Data Processing</h2>
      <p>
        When an email is sent to your temporary address, it is received by our
        servers and stored in our database. The email content is not modified,
        analyzed, or processed beyond basic storage.
      </p>
      <p>
        Email content is displayed to you when you view your mailbox. This
        happens in your browser — we do not process or analyze the content.
      </p>
    </section>

    <section>
      <h2>No Analytics</h2>
      <p>tsbin Mail does not use analytics services.</p>
      <p>We do not track how you use the service.</p>
      <p>We do not collect usage statistics.</p>
      <p>We do not implement telemetry of any kind.</p>
    </section>

    <section>
      <h2>No Cookies</h2>
      <p>tsbin Mail does not use cookies for tracking.</p>
      <p>
        Your browser may use localStorage to remember your created mailboxes for
        convenience. This data never leaves your device.
      </p>
    </section>

    <section>
      <h2>Third-Party Services</h2>
      <p>tsbin Mail uses Appwrite Cloud for database storage.</p>
      <p>
        Appwrite processes email data on our behalf. They do not have access to
        read email contents as data is not encrypted at rest.
      </p>
      <p>
        All data stored in Appwrite is automatically deleted after 24 hours.
      </p>
    </section>

    <section>
      <h2>IP Addresses</h2>
